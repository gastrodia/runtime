import {PluginContext} from './PluginContext';

//==========================================================
// <T>插件接口。</T>
//
// @reference
// @author maocy
// @version 160309
//==========================================================
export interface PluginFace {
   // 类型
   type: string;
   // 名称
   name: string;
   // 有效
   enable: boolean;
   // 描述
   description: string;

   //==========================================================
   // <T>创建处理。</T>
   //==========================================================
   create(context: PluginContext);

   //==========================================================
   // <T>激活处理。</T>
   //==========================================================
   active(context: PluginContext);

   //==========================================================
   // <T>取消激活处理。</T>
   //==========================================================
   deactive(context: PluginContext);

   //==========================================================
   // <T>销毁处理。</T>
   //==========================================================
   destroy(context: PluginContext);
}
